/**
  * @brief  The application entry point.
  * @retval int
 */
int main(void)
{
    /* MCU Configuration--------------------------------------------------------*/

    /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
    HAL_Init();

    /* Configure the system clock */
    SystemClock_Config();

    /* Initialize all configured peripherals */
    MX_GPIO_Init();
    MX_LPUART1_UART_Init();
    MX_I2C2_Init();
    MX_I2C3_Init();
    MX_TIM3_Init();
    MX_TIM17_Init();
    MX_TIM20_Init();

    /* Infinite loop */
    while (1)
    {
    }
}



